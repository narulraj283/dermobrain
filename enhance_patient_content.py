def process_file_enhanced(file_path: str) -> bool:
    """Process articles and enhance content to 800-1200 words."""

    print(f"\n{'='*80}")
    print(f"ENHANCING: {file_path}")
    print(f"{'='*80}\n")

    # Determine category
    if 'nails' in file_path:
        category = 'nails'
    elif 'pediatric' in file_path:
        category = 'pediatric'
    elif 'pre-post-op' in file_path:
        category = 'pre-post-op'
    else:
        print("ERROR: Cannot determine category")
        return False

    # Load articles
    with open(file_path, 'r', encoding='utf-8') as f:
        articles = json.load(f)

    print(f"Loaded {len(articles)} articles from {category}\n")

    # Process each article
    for idx, article in enumerate(articles, 1):
        title = article.get('title', 'Untitled')[:50]

        try:
            # Regenerate content with expansion
            patient_content = generate_expanded_patient_content(
                title=article['title'],
                category=category,
                original_content=article.get('content', '')
            )

            article['patient_content'] = patient_content

            # Verify
            wc = len(patient_content.split())
            status = "✓" if wc >= 800 else "⚠"
            print(f"[{idx}/{len(articles)}] {status} {title} - {wc} words")

        except Exception as e:
            print(f"[{idx}/{len(articles)}] ✗ {title} - ERROR: {str(e)[:50]}")

    # Save
    print(f"\nSaving enhanced articles to {file_path}...")
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(articles, f, indent=2, ensure_ascii=False)

    # Verify
    print(f"\nVERIFYING...")
    with open(file_path, 'r', encoding='utf-8') as f:
        saved = json.load(f)

    word_counts = []
    for art in saved:
        if 'patient_content' in art:
            wc = len(art['patient_content'].split())
            word_counts.append(wc)

    if word_counts:
        min_wc = min(word_counts)
        max_wc = max(word_counts)
        avg_wc = sum(word_counts) / len(word_counts)
        below_800 = sum(1 for w in word_counts if w < 800)

        print(f"Word count: Min={min_wc}, Max={max_wc}, Avg={avg_wc:.0f}")
        print(f"Below 800 words: {below_800}/{len(word_counts)}")

        if below_800 == 0:
            print(f"\n✓ ALL {len(word_counts)} ARTICLES NOW HAVE 800+ WORDS!")
            return True
        else:
            print(f"\n⚠ Still {below_800} articles below 800 words")
            return False

    return False


